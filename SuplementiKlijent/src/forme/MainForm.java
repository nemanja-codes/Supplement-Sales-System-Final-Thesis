    public void popuniSuplemente() {
        try {
            ArrayList<Suplement> suplementi = KlijentKontroler.getInstance().vratiSveSuplement();

            cmbSuplementi.removeAllItems();

            for (Suplement suplement : suplementi) {
                cmbSuplementi.addItem(suplement);
            }

        } catch (Exception ex) {
            Logger.getLogger(FormaNoviSastojak.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

