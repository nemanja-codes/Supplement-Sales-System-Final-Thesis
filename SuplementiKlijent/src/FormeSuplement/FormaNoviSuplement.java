    private void popuniJediniceMere() {
        try {
            ArrayList<JedinicaMere> jediniceMera = KlijentKontroler.getInstance().vratiSveJedinicaMere();

            cmbJediniceMere.removeAllItems();

            for (JedinicaMere jedinicaMere : jediniceMera) {
                cmbJediniceMere.addItem(jedinicaMere);
            }

        } catch (Exception ex) {
            Logger.getLogger(FormaNoviSuplement.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void popuniKategorije() {
        try {
            ArrayList<Kategorija> kategorije = KlijentKontroler.getInstance().vratiSveKategorija();

            cmbKategorija.removeAllItems();

            for (Kategorija kategorija : kategorije) {
                cmbKategorija.addItem(kategorija);
            }

        } catch (Exception ex) {
            Logger.getLogger(FormaNoviSuplement.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void popuniSastojke() {
        try {
            ArrayList<Sastojak> sastojci = KlijentKontroler.getInstance().vratiSveSastojak();

            cmbSastojak.removeAllItems();

            for (Sastojak sastojak : sastojci) {
                cmbSastojak.addItem(sastojak);
            }

        } catch (Exception ex) {
            Logger.getLogger(FormaNoviSuplement.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

