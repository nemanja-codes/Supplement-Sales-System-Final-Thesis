    private void btnDodajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDodajActionPerformed
        try {
            if (txtNaziv.getText().isEmpty() || txtKolicinaSuplementa.getText().isEmpty()
                    || txtCena.getText().isEmpty() || txtOpis.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Sva polja moraju biti popunjena!");
                return;
            }

            String naziv = txtNaziv.getText();
            String opis = txtOpis.getText();
            JedinicaMere jm = (JedinicaMere) cmbJediniceMere.getSelectedItem();
            Kategorija kategorija = (Kategorija) cmbKategorija.getSelectedItem();
            double kolicina = Double.parseDouble(txtKolicinaSuplementa.getText());
            double cena = Double.parseDouble(txtCena.getText());
            
            ModelTabeleStavkaSuplementa tm = (ModelTabeleStavkaSuplementa) tblSastojci.getModel();
            
            Suplement s = new Suplement(null, naziv, kolicina, cena, 
                    opis, kategorija, jm, tm.getLista());
            
            KlijentKontroler.getInstance().dodajSuplement(s);
            MainForm mf = (MainForm) getParent();
            mf.popuniSuplemente();
            JOptionPane.showMessageDialog(this, "Uspesno dodat suplement.");
            this.dispose();

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }//GEN-LAST:event_btnDodajActionPerformed

    private void btnDodajSastojakActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDodajSastojakActionPerformed

        if (txtKolicinaSastojka.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Morate uneti kolicinu!");
            return;
        }

        Sastojak s = (Sastojak) cmbSastojak.getSelectedItem();
        double kolicina = Double.parseDouble(txtKolicinaSastojka.getText());

        StavkaSuplementa ss = new StavkaSuplementa(null, -1, kolicina, s);

        ModelTabeleStavkaSuplementa tm = (ModelTabeleStavkaSuplementa) tblSastojci.getModel();
        tm.dodajStavku(ss);

    }//GEN-LAST:event_btnDodajSastojakActionPerformed

    private void btnObrisiSastojakActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnObrisiSastojakActionPerformed

        int row = tblSastojci.getSelectedRow();

        if (row >= 0) {
            ModelTabeleStavkaSuplementa tm = (ModelTabeleStavkaSuplementa) tblSastojci.getModel();
            tm.obrisiStavku(row);
        }

    }//GEN-LAST:event_btnObrisiSastojakActionPerformed

