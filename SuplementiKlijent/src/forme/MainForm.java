    private void txtKolicinaKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtKolicinaKeyReleased
        if (evt.getKeyChar() >= '0' && evt.getKeyChar() <= '9') {

            Suplement suplement = (Suplement) cmbSuplementi.getSelectedItem();
            int kolicina = Integer.parseInt(txtKolicina.getText());

            if (kolicina == 0) {
                txtKolicina.setText("");
                txtCena.setText("");
                return;
            }

            txtCena.setText(String.valueOf(suplement.getCena() * kolicina));

        } else {
            txtKolicina.setText("");
            txtCena.setText("");
        }
    }//GEN-LAST:event_txtKolicinaKeyReleased

    private void cmbSuplementiItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cmbSuplementiItemStateChanged

        if (cmbSuplementi.getSelectedItem() != null) {
            if (txtKolicina.getText().isEmpty()) {
                txtKolicina.setText("");
                return;
            }

            Suplement suplement = (Suplement) cmbSuplementi.getSelectedItem();

            int kolicina = Integer.parseInt(txtKolicina.getText());

            if (kolicina == 0) {
                txtKolicina.setText("");
                txtCena.setText("");
                return;
            }

            txtCena.setText(String.valueOf(suplement.getCena() * kolicina));
        }

    }//GEN-LAST:event_cmbSuplementiItemStateChanged

    private void btnDodajStavkuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDodajStavkuActionPerformed

        if (txtKolicina.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Morate uneti kolicinu!");
            return;
        }

        Suplement suplement = (Suplement) cmbSuplementi.getSelectedItem();
        int kolicina = Integer.parseInt(txtKolicina.getText());
        double cena = Double.parseDouble(txtCena.getText());

        StavkaRacuna sr = new StavkaRacuna(null, -1, kolicina, cena, suplement);

        ModelTabeleStavkeRacuna tm = (ModelTabeleStavkeRacuna) tblStavke.getModel();
        tm.dodajStavku(sr);

        ukupanIznos = tm.vratiUkupanIznos();
        txtUkupanIznos.setText(String.valueOf(ukupanIznos) + "din");
        txtKonacanIznos.setText(String.valueOf(ukupanIznos * 1.2) + "din");

    }//GEN-LAST:event_btnDodajStavkuActionPerformed

    private void btnObrisiStavkuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnObrisiStavkuActionPerformed

        int row = tblStavke.getSelectedRow();

        if (row >= 0) {
            ModelTabeleStavkeRacuna tm = (ModelTabeleStavkeRacuna) tblStavke.getModel();
            tm.obrisiStavku(row);

            ukupanIznos = tm.vratiUkupanIznos();
            txtUkupanIznos.setText(String.valueOf(ukupanIznos) + "din");
            txtKonacanIznos.setText(String.valueOf(ukupanIznos * 1.2) + "din");
        }

    }//GEN-LAST:event_btnObrisiStavkuActionPerformed

    private void btnSacuvajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSacuvajActionPerformed

        try {
            ModelTabeleStavkeRacuna tm = (ModelTabeleStavkeRacuna) tblStavke.getModel();

            Racun racun = new Racun(null, new Date(), ukupanIznos, 20,
                    ukupanIznos * 1.2, ulogovani, tm.getLista());

            KlijentKontroler.getInstance().dodajRacun(racun);
            txtKolicina.setText("");
            txtCena.setText("");
            txtUkupanIznos.setText("");
            txtKonacanIznos.setText("");
            tm.getLista().clear();
            tm.fireTableDataChanged();
            JOptionPane.showMessageDialog(this, "Uspesno sacuvan racun!");

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
//            Logger.getLogger(MainForm.class.getName()).log(Level.SEVERE, null, ex);
        }


    }//GEN-LAST:event_btnSacuvajActionPerformed

